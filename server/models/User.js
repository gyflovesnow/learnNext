const mongoose = require('mongoose');

const { Schema } = mongoose;

const mongoSchema = new Schema({
  slug: {
    type: String,
    required: true,
    unique: true,
  },
  createdAt: {
    type: Date,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  isAdmin: {
    type: Boolean,
    default: false,
  },
  displayName: String,
  avatarUrl: String,
});

const User = mongoose.model('User', mongoSchema);

module.exports = User;